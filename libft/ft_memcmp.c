#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;

	str1 = (unsigned char*)s1;
	str2 = (unsigned char*)s2;
	i = 0;
	if (n == 0)
		return (0);
	n = n - 1;
	while (str1[i] == str2[i] && i < n)
		i++;
	return ((unsigned char)str1[i] - str2[i]);
}
